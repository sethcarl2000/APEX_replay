TapexVDCHit::TapexVDCHit(   int plane, 
                            int wire, 
                            double rawTime, 
                            TapexEventHandler *_event ) 
    : fEvent{_event}
{   
  f_isRightArm = fEvent->ActiveArm(); 
  
  fPlane    = plane; 
  fRawTime  = rawTime; 
  fWireNum  = wire; 
  fWirePos  = VDC::WirePos( f_isRightArm, fPlane, fWireNum );
  
  fRealTime 
    = (f_isRightArm ? VDC::R_TDC_offsets[plane][wire] : VDC::L_TDC_offsets[plane][wire]) 
    - (VDC::TDC_resolution*fRawTime)
    - fEvent->GetS2Hit()->Time();
  
  //HRS::VDC::RealTime( f_isRightArm, fPlane, fWireNum, fRawTime ) - fEvent->GetS2Hit()->Time();
  fW = f_isRightArm ? VDC::R_plane_w[plane] : VDC::L_plane_w[fPlane]; 
}
